package marvelous_mashup.team29.ui_util.options;

/**
 * Enum for all different TextStyles
 * TextStyles can be added if a ttf is added to the assets/fonts folder, and implemented to the methods in the MarvelStyle.
 * This can easily be done by adding the font to the generateFonts() and getFont() method.
 * Watch out: to do this, you have to create a new BitmapFont.
 */
public enum TextStyleEnum {NORMAL, BOLD, COMIC}
