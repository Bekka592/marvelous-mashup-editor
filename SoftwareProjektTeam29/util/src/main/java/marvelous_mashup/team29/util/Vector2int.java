package marvelous_mashup.team29.util;

/**
 * This is a util class that contains a pair of 2 Integer values
 */
public class Vector2int {
    private int x;
    private int y;

    public Vector2int(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }
}
